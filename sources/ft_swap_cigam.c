#include "../includes/ft_nm.h"

int32_t	ft_swap_int32(int32_t x)
{
	int32_t tmp1;
	int32_t tmp2;
	int32_t tmp3;
	int32_t tmp4;

	tmp1 = (x & 0xFF000000) >> 24;
	tmp2 = (x & 0x000000FF) << 24;
	tmp3 = (x & 0x00FF0000) >> 8;
	tmp4 = (x & 0x0000FF00) << 8;
	return (tmp1 | tmp2 | tmp3 | tmp4);
}